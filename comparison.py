"""
Python Comparison Operators
---------------------------

Comparison operators are used to compare two values.
They always return a Boolean value: True or False.

List of Comparison Operators
----------------------------
==   Equal to
!=   Not equal to
>    Greater than
<    Less than
>=   Greater than or equal to
<=   Less than or equal to
"""

# =========================
# 1. Equal to (==)
# =========================
print("5 == 5 :", 5 == 5)     # True
print("5 == 3 :", 5 == 3)     # False


# =========================
# 2. Not Equal to (!=)
# =========================
print("5 != 3 :", 5 != 3)     # True
print("5 != 5 :", 5 != 5)     # False


# =========================
# 3. Greater Than (>)
# =========================
print("5 > 3 :", 5 > 3)       # True
print("3 > 5 :", 3 > 5)       # False


# =========================
# 4. Less Than (<)
# =========================
print("5 < 3 :", 5 < 3)       # False
print("3 < 5 :", 3 < 5)       # True


# =========================
# 5. Greater Than or Equal To (>=)
# =========================
print("5 >= 5 :", 5 >= 5)     # True
print("4 >= 5 :", 4 >= 5)     # False


# =========================
# 6. Less Than or Equal To (<=)
# =========================
print("5 <= 3 :", 5 <= 3)     # False
print("3 <= 5 :", 3 <= 5)     # True


"""
Important Notes:
----------------
1. Comparison operators are widely used in if-else conditions.
2. The result is always True or False.
3. They are essential for decision-making in Python programs.
"""
